a = int(input("Enter angle a: "))
b = int(input("Enter angle b: "))
c = int(input("Enter angle c: "))

# For angle a
if a == 90:
    print("Angle a is Right")
elif a > 90:
    print("Angle a is Obtuse")
else:
    print("Angle a is Acute")

# For angle b
if b == 90:
    print("Angle b is Right")
elif b > 90:
    print("Angle b is Obtuse")
else:
    print("Angle b is Acute")

# For angle c
if c == 90:
    print("Angle c is Right")
elif c > 90:
    print("Angle c is Obtuse")
else:
    print("Angle c is Acute")
